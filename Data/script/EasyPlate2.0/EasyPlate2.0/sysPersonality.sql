--个性化设置主表
CREATE TABLE sysPersonality_Mst(
	PersonalityGUID VARCHAR(50) NOT NULL PRIMARY KEY,
	UserGUID VARCHAR(50) NOT NULL,
	SkinName VARCHAR(100),--默认皮肤
	LangName VARCHAR(100),--语言
	EnableMDITab BIT NOT NULL DEFAULT 1,
	MDITabPosition INT NOT NULL DEFAULT 0,
	MDITabTextRation INT NOT NULL DEFAULT 0,
	MDITabCloseButton INT NOT NULL DEFAULT 1
)
CREATE UNIQUE INDEX index_UserGUID ON sysPersonality(UserGUID);

--个性化设置从表
CREATE TABLE sysPersonality_Dtl(
	GUID VARCHAR(50) NOT NULL PRIMARY KEY,
	PersonalityGUID VARCHAR(50) NOT NULL,
	Name VARCHAR(100) NOT NULL,
	EnableMDITab BIT NOT NULL DEFAULT 1,
	MDITabPosition INT NOT NULL DEFAULT 0,
	MDITabTextRation INT NOT NULL DEFAULT 0,
	MDITabCloseButton INT NOT NULL DEFAULT 1
)
